        <h2>CRICKET HISTORY</h2>
        <P>Early History
Origins: Cricket is believed to have originated in southeast England in the 16th century. The game was initially played by children before it gained popularity among adults.

First Recorded Match: The first recorded match took place in 1611 in Sussex, England.

First Laws of Cricket: The first known codification of the laws of cricket occurred in 1744. These laws have been periodically revised and updated by the Marylebone Cricket Club (MCC), which was founded in 1787.<P>
<P>18th and 19th Century
Growth in Popularity: Cricket grew in popularity in England, and the Hambledon Club (founded in the 1760s) became the sport's first formal club.

County Cricket: By the 18th century, cricket matches between counties began, with Sussex forming the first county club in 1839. County cricket became an official competition in the 1890s.

International Matches: The first international match was played between the USA and Canada in 1844.<P>
<P>20th Century
Test Cricket: Test cricket became the pinnacle of the sport. The Imperial Cricket Conference, now known as the International Cricket Council (ICC), was established in 1909 to govern international matches.

Introduction of Limited Overs Cricket: One-Day Internationals (ODIs) were introduced in the 1970s. The first ODI was played in 1971 between England and Australia.

World Cup: The first Cricket World Cup was held in 1975 in England. West Indies emerged as the first World Cup champions.<P>
